CREATE TABLE Users(ID BIGINT PRIMARY KEY NOT NULL AUTO_INCREMENT, login VARCHAR[20], password VARCHAR[64], mail VARCHAR[40]);
CREATE TABLE Admin(id BIGINT PRIMARY KEY NOT NULL AUTO_INCREMENT, login VARCHAR[20], password VARCHAR[64], mail VARCHAR[40]);
CREATE TABLE Artiste(id BIGINT PRIMARY KEY NOT NULL AUTO_INCREMENT, nom VARCHAR[30], epoque SMALLINT, genre VARCHAR[30]);
CREATE TABLE Instrument(id BIGINT PRIMARY KEY NOT NULL AUTO_INCREMENT, nom VARCHAR[30], logo VARCHAR[30]);
CREATE TABLE Morceau(id BIGINT PRIMARY KEY NOT NULL AUTO_INCREMENT, nom VARCHAR[30], tonalite VARCHAR[10], bpm SMALLINT, date_sortie SMALLINT, idA BIGINT, genre VARCHAR[30]);
CREATE TABLE Piste(id BIGINT PRIMARY KEY NOT NULL AUTO_INCREMENT, nom VARCHAR[30], idM BIGINT, idI BIGINT);
